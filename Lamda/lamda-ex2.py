x = lambda a, b : a * b
print(x(5, 6))

'''
x = lambda a, b: a * b:

This line defines a lambda function and assigns it to the variable x.
lambda a, b: This part indicates that the lambda function takes two arguments, a and b.
a * b: This is the expression that the lambda function will evaluate and return. It multiplies the two arguments a and b.
print(x(5, 6)):

This line calls the lambda function x with the arguments 5 and 6.
The function computes the expression 5 * 6, which equals 30.
The result 30 is then printed to the console.
Summary:
The lambda function x takes two arguments a and b, multiplies them, and returns the result.
When x(5, 6) is called, it multiplies 5 and 6, resulting in 30.
The print() function then outputs 30 to the console.

'''